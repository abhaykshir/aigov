def _index_doc(
    entries: list[dict],
    all_records: list[AISystemRecord],
    generated_at: str,
) -> str:
    buf = StringIO()
    w = buf.write

    w("# aigov Compliance Documentation Index\n\n")
    w("**EU AI Act Compliance Documentation**  \n")
    w(f"*Generated by aigov on {generated_at}*\n\n")
    w("---\n\n")

    w("## Generated Documents\n\n")
    if not entries:
        w("*No compliance documents were generated. All systems are MINIMAL RISK.*\n\n")
    else:
        w("| System | Risk Level | Document Type | File | Status |\n")
        w("|--------|-----------|---------------|------|--------|\n")
        for entry in entries:
            rec = entry["record"]
            risk = (rec.risk_classification.value.upper().replace("_", " ")
                    if rec.risk_classification else "UNKNOWN")
            fname = Path(entry["path"]).name
            doc_type = entry["doc_type"]
            w(f"| {rec.name} | {risk} | {doc_type} | [{fname}]({fname}) | DRAFT — requires completion |\n")
        w("\n")

    # List MINIMAL_RISK / unclassified systems that got no docs
    no_doc_records = [
        r for r in all_records
        if not any(e["record"] is r for e in entries)
    ]
    if no_doc_records:
        w("## Systems Requiring No Documentation\n\n")
        w(
            "The following systems were classified as MINIMAL RISK or could not be classified "
            "and do not require EU AI Act compliance documentation:\n\n"
        )
        w("| System | Provider | Risk Level | Location |\n")
        w("|--------|----------|-----------|----------|\n")
        for rec in no_doc_records:
            risk = (rec.risk_classification.value.upper().replace("_", " ")
                    if rec.risk_classification else "UNKNOWN")
            w(f"| {rec.name} | {rec.provider} | {risk} | `{rec.source_location}` |\n")
        w("\n")

    w("---\n\n")
    w("## Next Steps\n\n")
