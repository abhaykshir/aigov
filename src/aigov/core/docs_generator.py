from __future__ import annotations

import re
from datetime import datetime, timezone
from io import StringIO
from pathlib import Path

from aigov.core.models import AISystemRecord, RiskLevel

# Tags that may carry redacted credential material — never write to docs.
_SENSITIVE_TAGS = frozenset({"key_preview", "key_type"})


def _slug(name: str) -> str:
    """Filesystem-safe slug, max 60 chars."""
    return re.sub(r'[^\w]+', '_', name.lower()).strip('_')[:60]


def _source_slug(record: AISystemRecord) -> str:
    """Derive a slug from the source file stem (e.g. 'fraud_detection' from
    'demo\\analytics\\fraud_detection.py:2').  Falls back to _slug(record.name)
    when source_location contains no recognisable file name."""
    loc = record.source_location
    # Strip trailing line-number annotation (e.g. ":42" or "#L42")
    loc = re.sub(r'[:#]L?\d+$', '', loc)
    # Split on both / and \ so Windows-style paths work on Linux runners too.
    parts = re.split(r'[/\\]', loc)
    filename = next((p for p in reversed(parts) if p), '')
    stem = Path(filename).stem if filename else ''
    if stem and stem not in ('.', ''):
        return _slug(stem)
    return _slug(record.name)


def _unique_slug(base: str, used: dict[str, int]) -> str:
    if base not in used:
        used[base] = 1
        return base
    used[base] += 1
    return f"{base}_{used[base]}"


def _safe_tags(record: AISystemRecord) -> dict[str, str]:
    """Return tags with sensitive keys stripped."""
    return {k: v for k, v in record.tags.items() if k not in _SENSITIVE_TAGS}


def _ts(record: AISystemRecord) -> str:
    return record.discovery_timestamp.strftime("%Y-%m-%d")


def _generated_header(doc_type: str) -> str:
    now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    return (
        f"*Generated by aigov on {now}*  \n"
        f"*{doc_type} — requires human review and completion before submission*\n\n"
        "---\n\n"
    )


