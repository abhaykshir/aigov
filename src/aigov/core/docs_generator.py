class DocsGenerator:
    def generate(self, records: list[AISystemRecord], output_dir: str) -> list[str]:
        """Generate EU AI Act compliance documents for classified AI systems.

        Returns a list of file paths created (relative to output_dir).
        """
        out = Path(output_dir)
        out.mkdir(parents=True, exist_ok=True)

        generated_at = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
        created: list[str] = []
        entries: list[dict] = []
        used_slugs: dict[str, int] = {}

        for record in records:
            risk = record.risk_classification or RiskLevel.UNKNOWN
            slug = _unique_slug(_source_slug(record), used_slugs)

            if risk == RiskLevel.HIGH_RISK:
                fname = f"{slug}_annex_iv.md"
                content = _annex_iv(record)
                doc_type = "Annex IV Technical Documentation"
            elif risk == RiskLevel.LIMITED_RISK:
                fname = f"{slug}_transparency.md"
                content = _transparency_doc(record)
                doc_type = "Article 50 Transparency Obligations"
            elif risk == RiskLevel.PROHIBITED:
                fname = f"{slug}_prohibited.md"
                content = _prohibited_doc(record)
                doc_type = "Article 5 Prohibited Practice Notice"
            else:
                continue

            path = out / fname
            path.write_text(content, encoding="utf-8")
            full_path = str(path)
            created.append(full_path)
            entries.append({"record": record, "path": full_path, "doc_type": doc_type})

        index_content = _index_doc(entries, records, generated_at)
        index_path = out / "index.md"
        index_path.write_text(index_content, encoding="utf-8")
        created.append(str(index_path))

        return created
